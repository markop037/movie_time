class Movie {
    constructor(imdbId, title, releaseDate, trailerLink, poster, genres, backdrops){
        this.imdbId = imdbId;
        this.title = title;
        this.releaseDate = releaseDate;
        this.trailerLink = trailerLink;
        this.poster = poster;
        this.genres = genres;
        this.backdrops = backdrops;
    }
}